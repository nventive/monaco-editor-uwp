namespace Monaco.Editor
{ 
    /// <summary>
    /// End of line character preference.
    /// </summary>
    public enum EndOfLineSequence
    {
        /// <summary>
        /// Use carriage return and line feed (\r\n) as the end of line character.
        /// </summary>
        CRLF = 1,
        /// <summary>
        /// Use line feed (\n) as the end of line character.
        /// </summary>
        LF = 0
    }
}

